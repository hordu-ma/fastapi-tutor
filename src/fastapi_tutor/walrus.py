if (eggs := 7 + 5) == 12:
    print("We have 12 eggs")

print(eggs)

# ------------------------------
# 海象运算符 (Walrus operator, :=) 语法说明
#
# 1) 基本语法:
#    <name> := <expr>
#    会先计算 <expr>, 把结果赋值给 <name>, 同时这个表达式的值也等于该结果。
#
# 2) 常见用法: 在 if/while 等需要“边计算边判断”的场景, 避免重复计算。
#    例如:
#    if (n := len(items)) > 10:
#        ...
#
# 3) 优先级与括号:
#    为了可读性, 建议在 if/while 条件中像本例一样用括号包起来:
#    if (eggs := 7 + 5) == 12:
#
# 4) 作用域提示:
#    海象运算符的赋值发生在当前作用域里。本例在模块顶层赋值, 所以后续可以直接 print(eggs)。
#
# 5) 可读性建议:
#    若一行里同时做太多事会降低可读性; 当逻辑变复杂时, 拆成两行普通赋值通常更清晰。
