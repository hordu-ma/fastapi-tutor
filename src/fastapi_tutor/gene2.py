# 生成器表达式的示例

squares = (x**2 for x in range(10))

for square in squares:
    print(square)  # 输出 0, 1, 4, 9, ..., 81

# 解释:
# 1. 定义生成器表达式: squares = (x**2 for x in range(10))
#    - 这创建了一个生成器对象, 它会按需计算平方值。
# 2. 使用 for 循环迭代生成器:
#    - for 循环会自动调用生成器的 __next__() 方法, 获取下一个值, 直到 StopIteration 异常。
# 3. 这样可以节省内存, 因为不需要一次性生成所有平方值, 而是按需生成。
# 详细执行流程:
# 1. 创建生成器对象 squares:
#    - 初始化状态, 准备开始迭代。
# 2. 第一次迭代:
#    - 调用生成器的 __next__() 方法。
#    - 计算 0**2, 返回 0, 暂停状态。
# 3. 第二次迭代:
#    - 继续执行, 计算 1**2, 返回 1, 暂停状态。
# 4. 重复上述过程, 直到 x 达到 9。
# 5. 当 x 超过 9 时, 函数结束, 抛出 StopIteration 异常, 迭代终止。
